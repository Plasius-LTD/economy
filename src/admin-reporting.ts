/** Validates a bounded, normalized Admin activity query. */
export function assertAdminTokenActivityPageRequest(
  request: AdminTokenActivityPageRequestV1,
): void {
  assertExactKeys(
    request,
    [
      "schemaVersion",
      "limit",
      "cursor",
      "cursorBinding",
      "sort",
      "window",
      "filter",
      "audience",
      "pseudonymVersion",
    ],
    "Admin Token activity request",
  );
  economyAssert(
    request.schemaVersion === ECONOMY_CONTRACT_VERSION
      && Number.isSafeInteger(request.limit)
      && request.limit >= 1
      && request.limit <= ADMIN_TOKEN_MAXIMUM_PAGE_SIZE
      && ACTIVITY_SORTS.has(request.sort)
      && PSEUDONYM_AUDIENCES.has(request.audience)
      && PSEUDONYM_VERSION.test(request.pseudonymVersion),
    "INVALID_CONTRACT",
    "Unsupported or unbounded Admin Token activity request",
  );
  assertReportingWindow(request.window);
  if (request.filter !== undefined) {
    assertAdminTokenActivityFilter(request.filter);
  }
  economyAssert(
    (request.cursor === undefined) === (request.cursorBinding === undefined),
    "INVALID_CONTRACT",
    "Admin Token activity cursor and trusted binding must be provided together",
  );
  if (request.cursor === undefined || request.cursorBinding === undefined) {
    return;
  }
  assertOpaqueCursor(request.cursor);
  assertAdminTokenActivityCursorBinding(request.cursorBinding);
  economyAssert(
    request.cursorBinding.sort === request.sort
      && request.cursorBinding.window.fromInclusive
        === request.window.fromInclusive
      && request.cursorBinding.window.toExclusive === request.window.toExclusive
      && request.cursorBinding.audience === request.audience
      && request.cursorBinding.pseudonymVersion === request.pseudonymVersion
      && sameActivityFilter(request.cursorBinding.filter, request.filter),
    "INVALID_CONTRACT",
    "Admin Token cursor is not bound to the normalized query",
  );
}

function compareActivityEntries(
  left: AdminTokenActivityEntryV1,
  right: AdminTokenActivityEntryV1,
  sort: AdminTokenActivitySortV1,
): number {
  if (sort === "amount-absolute-desc") {
    const leftAmount = absolute(parseTokenSubunits(left.amount));
    const rightAmount = absolute(parseTokenSubunits(right.amount));
    if (leftAmount !== rightAmount) {
      return leftAmount > rightAmount ? -1 : 1;
    }
  }
  const leftTime = parseIsoTimestamp(left.occurredAt);
  const rightTime = parseIsoTimestamp(right.occurredAt);
  if (leftTime !== rightTime) {
    return leftTime > rightTime ? -1 : 1;
  }
  return compareTextDescending(left.rowAlias, right.rowAlias);
}

/** Validates page privacy metadata, bounds, uniqueness, and stable order. */
export function assertAdminTokenActivityPage(
  page: AdminTokenActivityPageV1,
): void {
  assertExactKeys(
    page,
    ["schemaVersion", "entries", "hasMore", "nextCursor", "metadata"],
    "Admin Token activity page",
  );
  assertExactKeys(
    page.metadata,
    [
      "schemaVersion",
      "generatedAt",
      "fromInclusive",
      "toExclusive",
      "sort",
      "filter",
      "pageLimit",
      "audience",
      "pseudonymVersion",
      "rawIdentifiersIncluded",
    ],
    "Admin Token activity metadata",
  );
  economyAssert(
    page.schemaVersion === ECONOMY_CONTRACT_VERSION
      && page.metadata.schemaVersion === ECONOMY_CONTRACT_VERSION
      && typeof page.hasMore === "boolean"
      && page.metadata.rawIdentifiersIncluded === false
      && ACTIVITY_SORTS.has(page.metadata.sort)
      && PSEUDONYM_AUDIENCES.has(page.metadata.audience)
      && PSEUDONYM_VERSION.test(page.metadata.pseudonymVersion)
      && Number.isSafeInteger(page.metadata.pageLimit)
      && page.metadata.pageLimit >= 1
      && page.metadata.pageLimit <= ADMIN_TOKEN_MAXIMUM_PAGE_SIZE,
    "INVALID_CONTRACT",
    "Unsupported Admin Token activity result metadata",
  );
  const window = assertReportingWindow({
    fromInclusive: page.metadata.fromInclusive,
    toExclusive: page.metadata.toExclusive,
  });
  if (page.metadata.filter !== undefined) {
    assertAdminTokenActivityFilter(page.metadata.filter);
  }
  parseIsoTimestamp(page.metadata.generatedAt);
  economyAssert(
    Array.isArray(page.entries)
      && page.entries.length <= page.metadata.pageLimit
      && page.hasMore === (page.nextCursor !== undefined)
      && (!page.hasMore || page.entries.length > 0),
    "INVALID_CONTRACT",
    "Admin Token activity page is unbounded or has inconsistent pagination",
  );
  if (page.nextCursor !== undefined) {
    assertOpaqueCursor(page.nextCursor);
  }

  const rowAliases = new Set<string>();
  let previous: AdminTokenActivityEntryV1 | undefined;
  for (const entry of page.entries) {
    assertAdminTokenActivityEntry(entry);
    const occurredAt = parseIsoTimestamp(entry.occurredAt);
    economyAssert(
      occurredAt >= window.from && occurredAt < window.to,
      "INVALID_TIME_WINDOW",
      "Admin Token activity falls outside the declared result window",
    );
    const filter = page.metadata.filter;
    economyAssert(
      filter === undefined
        || ((filter.activityTypes === undefined
          || filter.activityTypes.includes(entry.activityType))
        && (filter.statuses === undefined
          || filter.statuses.includes(entry.status))
        && (filter.sources === undefined
          || filter.sources.includes(entry.source))
        && (filter.subjectAlias === undefined
          || filter.subjectAlias === entry.subjectAlias)),
      "INVALID_CONTRACT",
      "Admin Token activity result contains a row outside its metadata filters",
    );
    economyAssert(
      !rowAliases.has(entry.rowAlias),
      "DUPLICATE_IDENTIFIER",
      "Admin Token activity page cannot repeat a row alias",
    );
    rowAliases.add(entry.rowAlias);
    if (previous !== undefined) {
      economyAssert(
        compareActivityEntries(previous, entry, page.metadata.sort) < 0,
        "INVALID_CONTRACT",
        "Admin Token activity page does not use its declared stable sort",
      );
    }
    previous = entry;
  }
}

