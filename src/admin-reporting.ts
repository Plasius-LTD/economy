function assertPlainObject(
  value: unknown,
  label: string,
): void {
  economyAssert(
    typeof value === "object" && value !== null && !Array.isArray(value),
    "INVALID_CONTRACT",
    `${label} must be an object`,
  );
}

function assertExactKeys(
  value: unknown,
  allowedKeys: readonly string[],
  label: string,
): void {
  assertPlainObject(value, label);
  const allowed = new Set(allowedKeys);
  economyAssert(
    Object.keys(value as Readonly<Record<string, unknown>>).every((key) =>
      allowed.has(key),
    ),
    "INVALID_CONTRACT",
    `${label} contains an unsupported or identifying property`,
  );
}

function hasControlCharacters(value: string): boolean {
  return [...value].some((character) => {
    const code = character.charCodeAt(0);
    return code <= 31 || code === 127;
  });
}

function assertSafeDisplayString(value: string, label: string): void {
  economyAssert(
    typeof value === "string"
      && value.trim().length > 0
      && value.length <= 128
      && !hasControlCharacters(value),
    "INVALID_CONTRACT",
    `${label} must be bounded and safe for display`,
  );
}

function assertAlias(value: string, label: string): void {
  economyAssert(
    typeof value === "string" && ALIAS.test(value),
    "INVALID_CONTRACT",
    `${label} must be an opaque pseudonymous alias`,
  );
}

function assertOpaqueCursor(value: string): void {
  economyAssert(
    typeof value === "string"
      && value.length > 0
      && value.length <= 512
      && !hasControlCharacters(value),
    "INVALID_CONTRACT",
    "Admin Token cursor must be a bounded opaque value",
  );
}

function assertUniqueValues<T extends string>(
  values: readonly T[],
  allowed: ReadonlySet<T>,
  label: string,
): void {
  economyAssert(
    Array.isArray(values),
    "INVALID_CONTRACT",
    `${label} must be a list`,
  );
  const unique = new Set(values);
  economyAssert(
    values.length > 0
      && values.length === unique.size
      && values.every((value) => allowed.has(value)),
    "INVALID_CONTRACT",
    `${label} must contain unique supported values`,
  );
}

function absolute(value: bigint): bigint {
  return value < 0n ? -value : value;
}

function compareTextDescending(left: string, right: string): number {
  if (left === right) {
    return 0;
  }
  return left > right ? -1 : 1;
}

function assertReportingWindow(
  window: AdminTokenReportingWindowV1,
  maximumDays = ADMIN_TOKEN_MAXIMUM_WINDOW_DAYS,
): { readonly from: number; readonly to: number } {
  assertExactKeys(
    window,
    ["fromInclusive", "toExclusive"],
    "Admin Token reporting window",
  );
  const from = parseIsoTimestamp(window.fromInclusive);
  const to = parseIsoTimestamp(window.toExclusive);
  economyAssert(
    from < to && to - from <= maximumDays * DAY_MILLISECONDS,
    "INVALID_TIME_WINDOW",
    `Admin Token reporting window must be positive and no longer than ${maximumDays} days`,
  );
  return { from, to };
}

/** Constructs the deterministic 30-day interactive default ending at `asOf`. */
export function createDefaultAdminTokenReportingWindow(
  asOf: IsoTimestamp,
): AdminTokenReportingWindowV1 {
  const to = parseIsoTimestamp(asOf);
  return {
    fromInclusive: new Date(
      to - ADMIN_TOKEN_DEFAULT_WINDOW_DAYS * DAY_MILLISECONDS,
    ).toISOString(),
    toExclusive: new Date(to).toISOString(),
  };
}

/** Validates one narrow row and rejects all undeclared identifier properties. */
export function assertAdminTokenActivityEntry(
  entry: AdminTokenActivityEntryV1,
): void {
  assertExactKeys(
    entry,
    [
      "schemaVersion",
      "occurredAt",
      "activityType",
      "status",
      "source",
      "amount",
      "safeLabel",
      "rowAlias",
      "subjectAlias",
    ],
    "Admin Token activity entry",
  );
  economyAssert(
    entry.schemaVersion === ECONOMY_CONTRACT_VERSION
      && ACTIVITY_TYPES.has(entry.activityType)
      && ACTIVITY_STATUSES.has(entry.status)
      && ACTIVITY_SOURCES.has(entry.source),
    "INVALID_CONTRACT",
    "Unsupported Admin Token activity contract",
  );
  parseIsoTimestamp(entry.occurredAt);
  const amount = parseTokenSubunits(entry.amount);
  economyAssert(
    amount !== 0n
      && (entry.activityType !== "acquisition" || amount > 0n)
      && (entry.activityType !== "spend" || amount < 0n),
    "INVALID_AMOUNT",
    "Admin Token activity amount has an invalid sign or zero value",
  );
  economyAssert(
    (entry.activityType === "failure") === (entry.status === "failed"),
    "INVALID_CONTRACT",
    "Failed rows must use the failure activity type",
  );
  assertSafeDisplayString(entry.safeLabel, "Admin Token activity label");
  assertAlias(entry.rowAlias, "Row alias");
  assertAlias(entry.subjectAlias, "Subject alias");
}

/** Validates a bounded, normalized Admin activity query. */
export function assertAdminTokenActivityPageRequest(
  request: AdminTokenActivityPageRequestV1,
): void {
  assertExactKeys(
    request,
    [
      "schemaVersion",
      "limit",
      "cursor",
      "sort",
      "window",
      "filter",
      "audience",
      "pseudonymVersion",
    ],
    "Admin Token activity request",
  );
  economyAssert(
    request.schemaVersion === ECONOMY_CONTRACT_VERSION
      && Number.isSafeInteger(request.limit)
      && request.limit >= 1
      && request.limit <= ADMIN_TOKEN_MAXIMUM_PAGE_SIZE
      && ACTIVITY_SORTS.has(request.sort)
      && PSEUDONYM_AUDIENCES.has(request.audience)
      && PSEUDONYM_VERSION.test(request.pseudonymVersion),
    "INVALID_CONTRACT",
    "Unsupported or unbounded Admin Token activity request",
  );
  assertReportingWindow(request.window);
  if (request.cursor !== undefined) {
    assertOpaqueCursor(request.cursor);
  }
  if (request.filter === undefined) {
    return;
  }
  assertExactKeys(
    request.filter,
    ["activityTypes", "statuses", "sources", "subjectAlias"],
    "Admin Token activity filter",
  );
  if (request.filter.activityTypes !== undefined) {
    assertUniqueValues(
      request.filter.activityTypes,
      ACTIVITY_TYPES,
      "Admin Token activity types",
    );
  }
  if (request.filter.statuses !== undefined) {
    assertUniqueValues(
      request.filter.statuses,
      ACTIVITY_STATUSES,
      "Admin Token activity statuses",
    );
  }
  if (request.filter.sources !== undefined) {
    assertUniqueValues(
      request.filter.sources,
      ACTIVITY_SOURCES,
      "Admin Token activity sources",
    );
  }
  if (request.filter.subjectAlias !== undefined) {
    assertAlias(request.filter.subjectAlias, "Subject alias filter");
  }
}

function compareActivityEntries(
  left: AdminTokenActivityEntryV1,
  right: AdminTokenActivityEntryV1,
  sort: AdminTokenActivitySortV1,
): number {
  if (sort === "amount-absolute-desc") {
    const leftAmount = absolute(parseTokenSubunits(left.amount));
    const rightAmount = absolute(parseTokenSubunits(right.amount));
    if (leftAmount !== rightAmount) {
      return leftAmount > rightAmount ? -1 : 1;
    }
  }
  const leftTime = parseIsoTimestamp(left.occurredAt);
  const rightTime = parseIsoTimestamp(right.occurredAt);
  if (leftTime !== rightTime) {
    return leftTime > rightTime ? -1 : 1;
  }
  return compareTextDescending(left.rowAlias, right.rowAlias);
}

/** Validates page privacy metadata, bounds, uniqueness, and stable order. */
export function assertAdminTokenActivityPage(
  page: AdminTokenActivityPageV1,
): void {
  assertExactKeys(
    page,
    ["schemaVersion", "entries", "hasMore", "nextCursor", "metadata"],
    "Admin Token activity page",
  );
  assertExactKeys(
    page.metadata,
    [
      "schemaVersion",
      "generatedAt",
      "fromInclusive",
      "toExclusive",
      "sort",
      "pageLimit",
      "audience",
      "pseudonymVersion",
      "rawIdentifiersIncluded",
    ],
    "Admin Token activity metadata",
  );
  economyAssert(
    page.schemaVersion === ECONOMY_CONTRACT_VERSION
      && page.metadata.schemaVersion === ECONOMY_CONTRACT_VERSION
      && typeof page.hasMore === "boolean"
      && page.metadata.rawIdentifiersIncluded === false
      && ACTIVITY_SORTS.has(page.metadata.sort)
      && PSEUDONYM_AUDIENCES.has(page.metadata.audience)
      && PSEUDONYM_VERSION.test(page.metadata.pseudonymVersion)
      && Number.isSafeInteger(page.metadata.pageLimit)
      && page.metadata.pageLimit >= 1
      && page.metadata.pageLimit <= ADMIN_TOKEN_MAXIMUM_PAGE_SIZE,
    "INVALID_CONTRACT",
    "Unsupported Admin Token activity result metadata",
  );
  const window = assertReportingWindow({
    fromInclusive: page.metadata.fromInclusive,
    toExclusive: page.metadata.toExclusive,
  });
  parseIsoTimestamp(page.metadata.generatedAt);
  economyAssert(
    Array.isArray(page.entries)
      && page.entries.length <= page.metadata.pageLimit
      && page.hasMore === (page.nextCursor !== undefined)
      && (!page.hasMore || page.entries.length > 0),
    "INVALID_CONTRACT",
    "Admin Token activity page is unbounded or has inconsistent pagination",
  );
  if (page.nextCursor !== undefined) {
    assertOpaqueCursor(page.nextCursor);
  }

  const rowAliases = new Set<string>();
  let previous: AdminTokenActivityEntryV1 | undefined;
  for (const entry of page.entries) {
    assertAdminTokenActivityEntry(entry);
    const occurredAt = parseIsoTimestamp(entry.occurredAt);
    economyAssert(
      occurredAt >= window.from && occurredAt < window.to,
      "INVALID_TIME_WINDOW",
      "Admin Token activity falls outside the declared result window",
    );
    economyAssert(
      !rowAliases.has(entry.rowAlias),
      "DUPLICATE_IDENTIFIER",
      "Admin Token activity page cannot repeat a row alias",
    );
    rowAliases.add(entry.rowAlias);
    if (previous !== undefined) {
      economyAssert(
        compareActivityEntries(previous, entry, page.metadata.sort) < 0,
        "INVALID_CONTRACT",
        "Admin Token activity page does not use its declared stable sort",
      );
    }
    previous = entry;
  }
}

/**
 * Proves that a result retained the server-normalized window, sort, audience,
 * pseudonym version, page bound, and filters of its request.
 */
export function assertAdminTokenActivityPageForRequest(
  page: AdminTokenActivityPageV1,
  request: AdminTokenActivityPageRequestV1,
): void {
  assertAdminTokenActivityPageRequest(request);
  assertAdminTokenActivityPage(page);
  economyAssert(
    page.metadata.fromInclusive === request.window.fromInclusive
      && page.metadata.toExclusive === request.window.toExclusive
      && page.metadata.sort === request.sort
      && page.metadata.pageLimit === request.limit
      && page.metadata.audience === request.audience
      && page.metadata.pseudonymVersion === request.pseudonymVersion,
    "INVALID_CONTRACT",
    "Admin Token activity result metadata does not match its normalized request",
  );
  const filter = request.filter;
  if (filter === undefined) {
    return;
  }
  economyAssert(
    page.entries.every(
      (entry) =>
        (filter.activityTypes === undefined
          || filter.activityTypes.includes(entry.activityType))
        && (filter.statuses === undefined
          || filter.statuses.includes(entry.status))
        && (filter.sources === undefined
          || filter.sources.includes(entry.source))
        && (filter.subjectAlias === undefined
          || filter.subjectAlias === entry.subjectAlias),
    ),
    "INVALID_CONTRACT",
    "Admin Token activity result contains a row outside its normalized filters",
  );
}

