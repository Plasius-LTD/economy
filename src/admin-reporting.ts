function assertPlainObject(
  value: unknown,
  label: string,
): asserts value is Readonly<Record<PropertyKey, unknown>> {
  economyAssert(
    typeof value === "object" && value !== null && !Array.isArray(value),
    "INVALID_CONTRACT",
    `${label} must be an object`,
  );
  const prototype = Object.getPrototypeOf(value);
  economyAssert(
    (prototype === Object.prototype || prototype === null)
      && !Reflect.has(value, "toJSON"),
    "INVALID_CONTRACT",
    `${label} must be a plain data object without serialization hooks`,
  );
}

function assertExactKeys(
  value: unknown,
  allowedKeys: readonly string[],
  label: string,
): void {
  assertPlainObject(value, label);
  const allowed = new Set(allowedKeys);
  const keys = Reflect.ownKeys(value);
  economyAssert(
    allowedKeys.every(
      (key) => !Reflect.has(value, key) || Object.hasOwn(value, key),
    ) && keys.every((key) => {
      if (typeof key !== "string" || !allowed.has(key)) {
        return false;
      }
      const descriptor = Object.getOwnPropertyDescriptor(value, key);
      return descriptor !== undefined
        && descriptor.enumerable
        && "value" in descriptor;
    }),
    "INVALID_CONTRACT",
    `${label} contains an unsupported, identifying, or non-data property`,
  );
}

function assertAlias(value: string, label: string): void {
  economyAssert(
    typeof value === "string" && ALIAS.test(value),
    "INVALID_CONTRACT",
    `${label} must be an opaque pseudonymous alias`,
  );
}

function assertOpaqueCursor(value: string): void {
  economyAssert(
    typeof value === "string" && OPAQUE_CURSOR.test(value),
    "INVALID_CONTRACT",
    "Admin Token cursor must be a bounded confidentiality-protected opaque value",
  );
}

function assertUniqueValues<T extends string>(
  values: readonly T[],
  allowed: ReadonlySet<T>,
  label: string,
): void {
  economyAssert(
    Array.isArray(values),
    "INVALID_CONTRACT",
    `${label} must be a list`,
  );
  const unique = new Set(values);
  economyAssert(
    values.length > 0
      && values.length === unique.size
      && values.every((value) => allowed.has(value)),
    "INVALID_CONTRACT",
    `${label} must contain unique supported values`,
  );
}

function absolute(value: bigint): bigint {
  return value < 0n ? -value : value;
}

function compareTextDescending(left: string, right: string): number {
  if (left === right) {
    return 0;
  }
  return left > right ? -1 : 1;
}

function assertReportingWindow(
  window: AdminTokenReportingWindowV1,
  maximumDays = ADMIN_TOKEN_MAXIMUM_WINDOW_DAYS,
): { readonly from: number; readonly to: number } {
  assertExactKeys(
    window,
    ["fromInclusive", "toExclusive"],
    "Admin Token reporting window",
  );
  const from = parseIsoTimestamp(window.fromInclusive);
  const to = parseIsoTimestamp(window.toExclusive);
  economyAssert(
    from < to && to - from <= maximumDays * DAY_MILLISECONDS,
    "INVALID_TIME_WINDOW",
    `Admin Token reporting window must be positive and no longer than ${maximumDays} days`,
  );
  return { from, to };
}

/** Constructs the deterministic 30-day interactive default ending at `asOf`. */
export function createDefaultAdminTokenReportingWindow(
  asOf: IsoTimestamp,
): AdminTokenReportingWindowV1 {
  const to = parseIsoTimestamp(asOf);
  return {
    fromInclusive: new Date(
      to - ADMIN_TOKEN_DEFAULT_WINDOW_DAYS * DAY_MILLISECONDS,
    ).toISOString(),
    toExclusive: new Date(to).toISOString(),
  };
}

/** Validates one narrow row and rejects all undeclared identifier properties. */
export function assertAdminTokenActivityEntry(
  entry: AdminTokenActivityEntryV1,
): void {
  assertExactKeys(
    entry,
    [
      "schemaVersion",
      "occurredAt",
      "activityType",
      "status",
      "source",
      "amount",
      "safeLabel",
      "rowAlias",
      "subjectAlias",
    ],
    "Admin Token activity entry",
  );
  economyAssert(
    entry.schemaVersion === ECONOMY_CONTRACT_VERSION
      && ACTIVITY_TYPES.has(entry.activityType)
      && ACTIVITY_STATUSES.has(entry.status)
      && ACTIVITY_SOURCES.has(entry.source),
    "INVALID_CONTRACT",
    "Unsupported Admin Token activity contract",
  );
  parseIsoTimestamp(entry.occurredAt);
  const amount = parseTokenSubunits(entry.amount);
  economyAssert(
    (amount !== 0n || entry.activityType === "failure")
      && (entry.activityType !== "acquisition" || amount > 0n)
      && (entry.activityType !== "spend" || amount < 0n),
    "INVALID_AMOUNT",
    "Admin Token activity amount has an invalid sign or zero value",
  );
  economyAssert(
    (entry.activityType === "failure") === (entry.status === "failed"),
    "INVALID_CONTRACT",
    "Failed rows must use the failure activity type",
  );
  economyAssert(
    SAFE_LABELS.has(entry.safeLabel)
      && SAFE_LABELS_BY_ACTIVITY_TYPE[entry.activityType]?.has(entry.safeLabel)
        === true,
    "INVALID_CONTRACT",
    "Admin Token activity label must use the approved non-identifying allowlist",
  );
  assertAlias(entry.rowAlias, "Row alias");
  assertAlias(entry.subjectAlias, "Subject alias");
  economyAssert(
    entry.rowAlias !== entry.subjectAlias,
    "INVALID_CONTRACT",
    "Row and subject aliases must use separate pseudonym domains",
  );
}

/** Validates a bounded, normalized Admin activity query. */
function assertAdminTokenActivityFilter(
  filter: AdminTokenActivityFilterV1,
): void {
  assertExactKeys(
    filter,
    [
      "activityTypes",
      "statuses",
      "sources",
      "subjectAlias",
    ],
    "Admin Token activity filter",
  );
  if (filter.activityTypes !== undefined) {
    assertUniqueValues(
      filter.activityTypes,
      ACTIVITY_TYPES,
      "Admin Token activity types",
    );
  }
  if (filter.statuses !== undefined) {
    assertUniqueValues(
      filter.statuses,
      ACTIVITY_STATUSES,
      "Admin Token activity statuses",
    );
  }
  if (filter.sources !== undefined) {
    assertUniqueValues(
      filter.sources,
      ACTIVITY_SOURCES,
      "Admin Token activity sources",
    );
  }
  if (filter.subjectAlias !== undefined) {
    assertAlias(filter.subjectAlias, "Subject alias filter");
  }
}

function sameOptionalValues<T extends string>(
  left: readonly T[] | undefined,
  right: readonly T[] | undefined,
): boolean {
  if (left === undefined || right === undefined) {
    return left === right;
  }
  return left.length === right.length
    && left.every((value) => right.includes(value));
}

function sameActivityFilter(
  left: AdminTokenActivityFilterV1 | undefined,
  right: AdminTokenActivityFilterV1 | undefined,
): boolean {
  if (left === undefined || right === undefined) {
    return left === right;
  }
  return sameOptionalValues(left.activityTypes, right.activityTypes)
    && sameOptionalValues(left.statuses, right.statuses)
    && sameOptionalValues(left.sources, right.sources)
    && left.subjectAlias === right.subjectAlias;
}

