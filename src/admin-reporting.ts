function lowerMedian(values: readonly bigint[]): bigint {
  const ordered = [...values].sort((left, right) =>
    left === right ? 0 : left < right ? -1 : 1,
  );
  const value = ordered[Math.floor((ordered.length - 1) / 2)];
  economyAssert(
    value !== undefined,
    "INVALID_CONTRACT",
    "Median requires at least one value",
  );
  return value;
}

function classifyAnomaly(options: {
  absoluteDelta: bigint;
  medianAbsoluteDeviation: bigint;
  minimumAbsoluteDelta: bigint;
  madMultiplierMilli: number;
}): Pick<
  AdminTokenAnomalyIndicatorV1,
  "status" | "explanationCode"
> {
  if (options.absoluteDelta < options.minimumAbsoluteDelta) {
    return {
      status: "normal",
      explanationCode: "below-absolute-minimum",
    };
  }
  if (options.medianAbsoluteDeviation === 0n) {
    return options.absoluteDelta > 0n
      ? {
          status: "irregular-spike",
          explanationCode: "zero-mad-absolute-threshold-exceeded",
        }
      : {
          status: "normal",
          explanationCode: "within-baseline-variation",
        };
  }
  const exceedsBaseline =
    options.absoluteDelta * 1_000n
    >= options.medianAbsoluteDeviation * BigInt(options.madMultiplierMilli);
  return exceedsBaseline
    ? {
        status: "irregular-spike",
        explanationCode: "median-mad-and-absolute-threshold-exceeded",
      }
    : {
        status: "normal",
        explanationCode: "within-baseline-variation",
      };
}

/**
 * Creates an exact, explainable same-window median/MAD indicator. V1 uses the
 * lower median so no fractional TokenSubunit is introduced for 28 samples.
 */
export function createAdminTokenAnomalyIndicator(
  input: CreateAdminTokenAnomalyIndicatorInputV1,
): AdminTokenAnomalyIndicatorV1 {
  assertExactKeys(
    input,
    [
      "observedAmount",
      "baselineAmounts",
      "minimumAbsoluteDelta",
      "madMultiplierMilli",
    ],
    "Admin Token anomaly input",
  );
  economyAssert(
    Array.isArray(input.baselineAmounts)
      && input.baselineAmounts.length === ADMIN_TOKEN_ANOMALY_BASELINE_DAYS
      && Number.isSafeInteger(input.madMultiplierMilli)
      && input.madMultiplierMilli >= 1
      && input.madMultiplierMilli <= 1_000_000,
    "INVALID_CONTRACT",
    "Admin Token anomaly input requires 28 baselines and a bounded fixed-point multiplier",
  );
  const observed = parseTokenSubunits(input.observedAmount);
  const baseline = input.baselineAmounts.map((amount) =>
    parseTokenSubunits(amount),
  );
  const minimum = parseTokenSubunits(input.minimumAbsoluteDelta);
  economyAssert(
    minimum > 0n,
    "INVALID_AMOUNT",
    "Admin Token anomaly absolute minimum must be positive",
  );
  const median = lowerMedian(baseline);
  const medianAbsoluteDeviation = lowerMedian(
    baseline.map((amount) => absolute(amount - median)),
  );
  const deviation = observed - median;
  const classification = classifyAnomaly({
    absoluteDelta: absolute(deviation),
    medianAbsoluteDeviation,
    minimumAbsoluteDelta: minimum,
    madMultiplierMilli: input.madMultiplierMilli,
  });
  return {
    method: "same-window-median-mad-v1",
    ...classification,
    baselineWindowCount: ADMIN_TOKEN_ANOMALY_BASELINE_DAYS,
    medianAmount: serializeTokenSubunits(median),
    medianAbsoluteDeviation: serializeTokenSubunits(medianAbsoluteDeviation),
    minimumAbsoluteDelta: input.minimumAbsoluteDelta,
    deviationFromMedian: serializeTokenSubunits(deviation),
    madMultiplierMilli: input.madMultiplierMilli,
  };
}

function assertAdminTokenAnomalyIndicator(
  anomaly: AdminTokenAnomalyIndicatorV1,
  observedAmount: TokenSubunitString,
): void {
  assertExactKeys(
    anomaly,
    [
      "method",
      "status",
      "baselineWindowCount",
      "medianAmount",
      "medianAbsoluteDeviation",
      "minimumAbsoluteDelta",
      "deviationFromMedian",
      "madMultiplierMilli",
      "explanationCode",
    ],
    "Admin Token anomaly indicator",
  );
  const observed = parseTokenSubunits(observedAmount);
  const median = parseTokenSubunits(anomaly.medianAmount);
  const mad = parseTokenSubunits(anomaly.medianAbsoluteDeviation);
  const minimum = parseTokenSubunits(anomaly.minimumAbsoluteDelta);
  const deviation = parseTokenSubunits(anomaly.deviationFromMedian);
  economyAssert(
    anomaly.method === "same-window-median-mad-v1"
      && anomaly.baselineWindowCount === ADMIN_TOKEN_ANOMALY_BASELINE_DAYS
      && mad >= 0n
      && minimum > 0n
      && Number.isSafeInteger(anomaly.madMultiplierMilli)
      && anomaly.madMultiplierMilli >= 1
      && anomaly.madMultiplierMilli <= 1_000_000
      && deviation === observed - median,
    "INVALID_CONTRACT",
    "Admin Token anomaly arithmetic or method is invalid",
  );
  const expected = classifyAnomaly({
    absoluteDelta: absolute(deviation),
    medianAbsoluteDeviation: mad,
    minimumAbsoluteDelta: minimum,
    madMultiplierMilli: anomaly.madMultiplierMilli,
  });
  economyAssert(
    anomaly.status === expected.status
      && anomaly.explanationCode === expected.explanationCode,
    "INVALID_CONTRACT",
    "Admin Token anomaly classification is inconsistent with its explanation",
  );
}

function assertTrendBucketAlignment(
  start: number,
  end: number,
  granularity: AdminTokenTrendGranularityV1,
): void {
  const expectedDuration =
    granularity === "hour" ? HOUR_MILLISECONDS : DAY_MILLISECONDS;
  const date = new Date(start);
  const aligned =
    date.getUTCMinutes() === 0
    && date.getUTCSeconds() === 0
    && date.getUTCMilliseconds() === 0
    && (granularity === "hour" || date.getUTCHours() === 0);
  economyAssert(
    aligned && end - start === expectedDuration,
    "INVALID_TIME_WINDOW",
    "Admin Token trend bucket is not aligned to its declared granularity",
  );
}

function assertTrendAggregate(
  aggregate: AdminTokenTrendAggregateV1,
): void {
  assertPlainObject(aggregate, "Admin Token trend aggregate");
  if (aggregate.visibility === "suppressed") {
    assertExactKeys(
      aggregate,
      ["visibility", "reason", "threshold"],
      "Suppressed Admin Token trend aggregate",
    );
    economyAssert(
      aggregate.reason === "cohort-below-minimum"
        && aggregate.threshold === ADMIN_TOKEN_MINIMUM_COHORT_SIZE,
      "INVALID_CONTRACT",
      "Suppressed Admin Token trend aggregate has invalid privacy metadata",
    );
    return;
  }
  assertExactKeys(
    aggregate,
    [
      "visibility",
      "distinctSubjects",
      "activityCount",
      "signedAmount",
    ],
    "Reported Admin Token trend aggregate",
  );
  parseTokenSubunits(aggregate.signedAmount);
  economyAssert(
    aggregate.visibility === "reported"
      && Number.isSafeInteger(aggregate.distinctSubjects)
      && aggregate.distinctSubjects >= ADMIN_TOKEN_MINIMUM_COHORT_SIZE
      && Number.isSafeInteger(aggregate.activityCount)
      && aggregate.activityCount >= aggregate.distinctSubjects,
    "INVALID_CONTRACT",
    "Reported Admin Token trend aggregate is invalid or below the privacy threshold",
  );
}

/** Validates suppression, bucket bounds, exact arithmetic, and anomaly details. */
export function assertAdminTokenTrendResult(
  result: AdminTokenTrendResultV1,
): void {
  assertExactKeys(
    result,
    ["schemaVersion", "series", "metadata"],
    "Admin Token trend result",
  );
  assertExactKeys(
    result.metadata,
    [
      "schemaVersion",
      "generatedAt",
      "fromInclusive",
      "toExclusive",
      "granularity",
      "minimumCohortSize",
      "anomalyBaselineDays",
      "rawIdentifiersIncluded",
    ],
    "Admin Token trend metadata",
  );
  economyAssert(
    result.schemaVersion === ECONOMY_CONTRACT_VERSION
      && result.metadata.schemaVersion === ECONOMY_CONTRACT_VERSION
      && TREND_GRANULARITIES.has(result.metadata.granularity)
      && result.metadata.minimumCohortSize
        === ADMIN_TOKEN_MINIMUM_COHORT_SIZE
      && result.metadata.anomalyBaselineDays
        === ADMIN_TOKEN_ANOMALY_BASELINE_DAYS
      && result.metadata.rawIdentifiersIncluded === false
      && Array.isArray(result.series)
      && result.series.length <= ADMIN_TOKEN_MAXIMUM_TREND_POINTS,
    "INVALID_CONTRACT",
    "Unsupported or unbounded Admin Token trend result",
  );
  const window = assertReportingWindow(
    {
      fromInclusive: result.metadata.fromInclusive,
      toExclusive: result.metadata.toExclusive,
    },
    result.metadata.granularity === "hour"
      ? ADMIN_TOKEN_MAXIMUM_HOURLY_WINDOW_DAYS
      : ADMIN_TOKEN_MAXIMUM_WINDOW_DAYS,
  );
  parseIsoTimestamp(result.metadata.generatedAt);

  const pointKeys = new Set<string>();
  let previousKey: string | undefined;
  for (const point of result.series) {
    assertExactKeys(
      point,
      [
        "schemaVersion",
        "bucketStart",
        "bucketEnd",
        "activityType",
        "aggregate",
        "anomaly",
      ],
      "Admin Token trend point",
    );
    economyAssert(
      point.schemaVersion === ECONOMY_CONTRACT_VERSION
        && ACTIVITY_TYPES.has(point.activityType),
      "INVALID_CONTRACT",
      "Unsupported Admin Token trend point",
    );
    const start = parseIsoTimestamp(point.bucketStart);
    const end = parseIsoTimestamp(point.bucketEnd);
    economyAssert(
      start >= window.from && end <= window.to,
      "INVALID_TIME_WINDOW",
      "Admin Token trend bucket falls outside the declared result window",
    );
    assertTrendBucketAlignment(start, end, result.metadata.granularity);
    assertTrendAggregate(point.aggregate);
    economyAssert(
      (point.aggregate.visibility === "reported")
        === (point.anomaly !== undefined),
      "INVALID_CONTRACT",
      "Only reported Admin Token cohorts may include an anomaly indicator",
    );
    if (point.anomaly !== undefined
      && point.aggregate.visibility === "reported") {
      assertAdminTokenAnomalyIndicator(
        point.anomaly,
        point.aggregate.signedAmount,
      );
    }
    const key = `${point.bucketStart}\u0000${point.activityType}`;
    economyAssert(
      !pointKeys.has(key) && (previousKey === undefined || previousKey < key),
      "DUPLICATE_IDENTIFIER",
      "Admin Token trend points must be unique and stably ordered",
    );
    pointKeys.add(key);
    previousKey = key;
  }
}
