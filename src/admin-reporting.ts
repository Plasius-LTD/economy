/**
 * Returns a conventional median for values already expressed in half
 * TokenSubunits. Inputs produced by this module guarantee an integral result.
 */
function medianHalfSubunits(values: readonly bigint[]): bigint {
  const ordered = [...values].sort((left, right) =>
    left === right ? 0 : left < right ? -1 : 1,
  );
  const lower = ordered[Math.floor((ordered.length - 1) / 2)];
  const upper = ordered[Math.floor(ordered.length / 2)];
  economyAssert(
    lower !== undefined && upper !== undefined,
    "INVALID_CONTRACT",
    "Median requires at least one value",
  );
  const sum = lower + upper;
  economyAssert(
    sum % 2n === 0n,
    "INVALID_AMOUNT",
    "Median cannot be represented in exact half TokenSubunits",
  );
  return sum / 2n;
}

function serializeExactStatistic(
  halfSubunits: bigint,
): AdminTokenExactStatisticV1 {
  economyAssert(
    absolute(halfSubunits) <= MAXIMUM_STATISTIC_HALF_SUBUNITS,
    "AMOUNT_OUT_OF_RANGE",
    "Admin Token statistic exceeds its exact bounded representation",
  );
  if (halfSubunits % 2n === 0n) {
    return {
      numerator: (halfSubunits / 2n).toString(10),
      denominator: 1,
    };
  }
  return {
    numerator: halfSubunits.toString(10),
    denominator: 2,
  };
}

function parseExactStatistic(
  value: AdminTokenExactStatisticV1,
  label: string,
): bigint {
  assertExactKeys(value, ["numerator", "denominator"], label);
  economyAssert(
    typeof value.numerator === "string"
      && value.numerator.length <= 22
      && EXACT_STATISTIC_INTEGER.test(value.numerator)
      && (value.denominator === 1 || value.denominator === 2),
    "INVALID_AMOUNT",
    `${label} must be an exact bounded rational TokenSubunit value`,
  );
  const numerator = BigInt(value.numerator);
  economyAssert(
    value.denominator === 1 || numerator % 2n !== 0n,
    "INVALID_AMOUNT",
    `${label} must use a reduced exact representation`,
  );
  const halfSubunits =
    numerator * BigInt(2 / value.denominator);
  economyAssert(
    absolute(halfSubunits) <= MAXIMUM_STATISTIC_HALF_SUBUNITS,
    "AMOUNT_OUT_OF_RANGE",
    `${label} exceeds its exact bounded representation`,
  );
  return halfSubunits;
}

function classifyAnomaly(options: {
  absoluteDeltaHalfSubunits: bigint;
  medianAbsoluteDeviationHalfSubunits: bigint;
  minimumAbsoluteDeltaHalfSubunits: bigint;
  madMultiplierMilli: number;
}): Pick<
  AdminTokenAnomalyIndicatorV1,
  "status" | "explanationCode"
> {
  if (
    options.absoluteDeltaHalfSubunits
      < options.minimumAbsoluteDeltaHalfSubunits
  ) {
    return {
      status: "normal",
      explanationCode: "below-absolute-minimum",
    };
  }
  if (options.medianAbsoluteDeviationHalfSubunits === 0n) {
    return options.absoluteDeltaHalfSubunits > 0n
      ? {
          status: "irregular-spike",
          explanationCode: "zero-mad-absolute-threshold-exceeded",
        }
      : {
          status: "normal",
          explanationCode: "within-baseline-variation",
        };
  }
  const exceedsBaseline =
    options.absoluteDeltaHalfSubunits * 1_000n
    >= options.medianAbsoluteDeviationHalfSubunits
      * BigInt(options.madMultiplierMilli);
  return exceedsBaseline
    ? {
        status: "irregular-spike",
        explanationCode: "median-mad-and-absolute-threshold-exceeded",
      }
    : {
        status: "normal",
        explanationCode: "within-baseline-variation",
      };
}

/**
 * Creates an exact, explainable conventional same-window median/MAD indicator.
 * Exact rational statistics avoid floating-point and preserve half-TokenSubunit
 * results from the two even-sized median operations.
 */
export function createAdminTokenAnomalyIndicator(
  input: CreateAdminTokenAnomalyIndicatorInputV1,
): AdminTokenAnomalyIndicatorV1 {
  assertExactKeys(
    input,
    [
      "observedAmount",
      "baselineSamples",
      "minimumAbsoluteDelta",
      "madMultiplierMilli",
    ],
    "Admin Token anomaly input",
  );
  economyAssert(
    Array.isArray(input.baselineSamples)
      && input.baselineSamples.length === ADMIN_TOKEN_ANOMALY_BASELINE_DAYS
      && Number.isSafeInteger(input.madMultiplierMilli)
      && input.madMultiplierMilli >= 1
      && input.madMultiplierMilli <= 1_000_000,
    "INVALID_CONTRACT",
    "Admin Token anomaly input requires 28 baselines and a bounded fixed-point multiplier",
  );
  const observed = parseTokenSubunits(input.observedAmount);
  const baseline = input.baselineSamples.map((sample) => {
    assertExactKeys(
      sample,
      ["amount", "distinctSubjects"],
      "Admin Token anomaly baseline sample",
    );
    economyAssert(
      Number.isSafeInteger(sample.distinctSubjects)
        && sample.distinctSubjects >= ADMIN_TOKEN_MINIMUM_COHORT_SIZE,
      "INVALID_CONTRACT",
      "Admin Token anomaly baselines must exclude privacy-suppressed cohorts",
    );
    return parseTokenSubunits(sample.amount);
  });
  const minimum = parseTokenSubunits(input.minimumAbsoluteDelta);
  economyAssert(
    minimum > 0n,
    "INVALID_AMOUNT",
    "Admin Token anomaly absolute minimum must be positive",
  );
  const baselineHalfSubunits = baseline.map((amount) => amount * 2n);
  const medianHalf = medianHalfSubunits(baselineHalfSubunits);
  const medianAbsoluteDeviationHalf = medianHalfSubunits(
    baselineHalfSubunits.map((amount) => absolute(amount - medianHalf)),
  );
  const deviationHalf = observed * 2n - medianHalf;
  const classification = classifyAnomaly({
    absoluteDeltaHalfSubunits: absolute(deviationHalf),
    medianAbsoluteDeviationHalfSubunits:
      medianAbsoluteDeviationHalf,
    minimumAbsoluteDeltaHalfSubunits: minimum * 2n,
    madMultiplierMilli: input.madMultiplierMilli,
  });
  return {
    method: "same-window-median-mad-v1",
    ...classification,
    baselineWindowCount: ADMIN_TOKEN_ANOMALY_BASELINE_DAYS,
    medianAmount: serializeExactStatistic(medianHalf),
    medianAbsoluteDeviation:
      serializeExactStatistic(medianAbsoluteDeviationHalf),
    minimumAbsoluteDelta: input.minimumAbsoluteDelta,
    deviationFromMedian: serializeExactStatistic(deviationHalf),
    madMultiplierMilli: input.madMultiplierMilli,
  };
}

/** Creates a non-identifying explanation when 28 eligible baselines do not exist. */
export function createAdminTokenAnomalyUnavailable(
  input: CreateAdminTokenAnomalyUnavailableInputV1,
): AdminTokenAnomalyUnavailableV1 {
  assertExactKeys(
    input,
    ["reason", "eligibleBaselineWindowCount"],
    "Unavailable Admin Token anomaly input",
  );
  economyAssert(
    ANOMALY_UNAVAILABLE_REASONS.has(input.reason)
      && Number.isSafeInteger(input.eligibleBaselineWindowCount)
      && input.eligibleBaselineWindowCount >= 0
      && input.eligibleBaselineWindowCount
        < ADMIN_TOKEN_ANOMALY_BASELINE_DAYS,
    "INVALID_CONTRACT",
    "Unavailable Admin Token anomaly requires a bounded reason and eligible baseline count",
  );
  return {
    method: "same-window-median-mad-v1",
    status: "unavailable",
    reason: input.reason,
    eligibleBaselineWindowCount: input.eligibleBaselineWindowCount,
    requiredBaselineWindowCount: ADMIN_TOKEN_ANOMALY_BASELINE_DAYS,
  };
}

function assertAdminTokenAnomalyIndicator(
  anomaly: AdminTokenAnomalyIndicatorV1,
  observedAmount: TokenSubunitString,
): void {
  assertExactKeys(
    anomaly,
    [
      "method",
      "status",
      "baselineWindowCount",
      "medianAmount",
      "medianAbsoluteDeviation",
      "minimumAbsoluteDelta",
      "deviationFromMedian",
      "madMultiplierMilli",
      "explanationCode",
    ],
    "Admin Token anomaly indicator",
  );
  const observed = parseTokenSubunits(observedAmount);
  const medianHalf = parseExactStatistic(
    anomaly.medianAmount,
    "Admin Token anomaly median",
  );
  const madHalf = parseExactStatistic(
    anomaly.medianAbsoluteDeviation,
    "Admin Token anomaly median absolute deviation",
  );
  const minimum = parseTokenSubunits(anomaly.minimumAbsoluteDelta);
  const deviationHalf = parseExactStatistic(
    anomaly.deviationFromMedian,
    "Admin Token anomaly deviation",
  );
  economyAssert(
    anomaly.method === "same-window-median-mad-v1"
      && anomaly.baselineWindowCount === ADMIN_TOKEN_ANOMALY_BASELINE_DAYS
      && medianHalf >= TOKEN_SUBUNITS_MIN * 2n
      && medianHalf <= TOKEN_SUBUNITS_MAX * 2n
      && madHalf >= 0n
      && minimum > 0n
      && Number.isSafeInteger(anomaly.madMultiplierMilli)
      && anomaly.madMultiplierMilli >= 1
      && anomaly.madMultiplierMilli <= 1_000_000
      && deviationHalf === observed * 2n - medianHalf,
    "INVALID_CONTRACT",
    "Admin Token anomaly arithmetic or method is invalid",
  );
  const expected = classifyAnomaly({
    absoluteDeltaHalfSubunits: absolute(deviationHalf),
    medianAbsoluteDeviationHalfSubunits: madHalf,
    minimumAbsoluteDeltaHalfSubunits: minimum * 2n,
    madMultiplierMilli: anomaly.madMultiplierMilli,
  });
  economyAssert(
    anomaly.status === expected.status
      && anomaly.explanationCode === expected.explanationCode,
    "INVALID_CONTRACT",
    "Admin Token anomaly classification is inconsistent with its explanation",
  );
}

